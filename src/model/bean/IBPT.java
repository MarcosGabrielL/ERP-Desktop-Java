package model.bean;

/**
 *
 * @author Marcos
 */
public class IBPT {
    
    private String codigo;
    private String federali;
    private String federaln;
    private String estadual;
    private String municipal;
    private String descricao;

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getFederali() {
        return federali;
    }

    public void setFederali(String federali) {
        this.federali = federali;
    }

    public String getFederaln() {
        return federaln;
    }

    public void setFederaln(String federaln) {
        this.federaln = federaln;
    }

    public String getEstadual() {
        return estadual;
    }

    public void setEstadual(String estadual) {
        this.estadual = estadual;
    }

    public String getMunicipal() {
        return municipal;
    }

    public void setMunicipal(String municipal) {
        this.municipal = municipal;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }
    
    
    
}
